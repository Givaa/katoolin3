def get_tool_name(tool):
    """Get display name from a tool entry."""
    return tool


def get_category_names():
    """Return dict of {id: name} for all categories."""
    return {k: v["name"] for k, v in CATEGORIES.items()}


def get_tools(category_id):
    """Return list of tools for a category."""
    return CATEGORIES[category_id]["tools"]


def count_all_tools():
    """Return total number of tools across all categories."""
    return sum(len(c["tools"]) for c in CATEGORIES.values())
